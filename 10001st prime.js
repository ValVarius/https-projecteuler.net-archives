// What is the 10 001st prime number?

let list =[];
let counter = 1;

while(list.length < 10001){
    if(isPrime(counter)){
        list.push(counter);
    }
    counter++;
}


function isPrime(n)
{

  if (n===1)
  {
    return false;
  }
  else if(n === 2)
  {
    return true;
  }else
  {
    for(let x = 2; x < n; x++)
    {
      if(n % x === 0)
      {
        return false;
      }
    }
    return true;  
  }
}

